<div class="bonus">  
<p>
The game window features three walls: one on the left, one on the right, and one at the top. These walls ensure that the ball stays within the playable area. Bricks are strategically positioned throughout the screen, each with a specific number of "lives" indicated by their color. The color changes as the bricks lose lives: blue (5 lives), green (4 lives), pink (3 lives), purple (2 lives), and yellow (1 life).
</p>
<p>
When the ball collides with a brick, the brick loses one life. Once a brick's life count reaches zero, it is destroyed. You earn 5 points for each brick you destroy. Additionally, three bonus/malus items can be dropped by destroying bricks. These items include:
</p>

<p>
1- Multiply Bullet by 3 : Increases the number of bullets your paddle can shoot by three, allowing you to demolish bricks more efficiently.
</p>

<p>
2- Glue the Paddle : Temporarily prevents the ball from bouncing off the paddle, keeping it "stuck" until you decide to release it. This power-up is similar to the ball's initial state at the start of the game.
</p>
<p>
3- Fire Bullet : Transforms the ball into an unstoppable force, destroying any bricks it encounters without bouncing off them.
</p>
<p>
4- Slow the Balls : Decreases the speed of all balls in play by 33%, making them easier to track and hit.
</p>
<p>
5- Increase Ball Speed : Boosts the speed of all balls in play by 50%, intensifying the gameplay and adding an extra challenge.
</p>
<p>
To activate these bonus/malus items, you must catch them with your paddle as they fall. Each bonus you catch earns you 7 points.
</p>
