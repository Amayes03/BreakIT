<p>
In Break/IT , your objective is to demolish all the bricks on the screen using a bouncing ball and a paddle. At the start of the game, you have three lives. If the ball escapes past your paddle and reaches the bottom of the screen, you lose a life. You can continue playing until you run out of lives. You control the paddle's horizontal movement using the left and right arrow keys on your keyboard. The paddle is anchored to the bottom of the screen and acts as a platform to bounce the ball.
</p>
